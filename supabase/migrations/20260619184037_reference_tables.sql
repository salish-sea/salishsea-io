-- =====================================================================
-- 3. public.organizations
-- Parent institutions for named collections. url NOT NULL per ORG-01.
-- rights_holder_text is the display string used in EML associatedParty (Phase 12).
-- Organizations MUST be seeded before collections (Pitfall 3: FK subquery order).
-- =====================================================================
CREATE TABLE public.organizations (
  id                 INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  slug               VARCHAR(50)  NOT NULL UNIQUE,
  name               VARCHAR(200) NOT NULL,
  url                TEXT         NOT NULL,
  rights_holder_text VARCHAR(200) NOT NULL
);

ALTER TABLE public.organizations ENABLE ROW LEVEL SECURITY;


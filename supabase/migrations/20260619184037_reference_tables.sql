-- =====================================================================
-- 4. public.collections
-- Shared registry of data channels — not 1:1 with providers (D-02).
-- kind nullable: stubs have NULL kind until Phase 11 decodes them (D-09).
-- organization_id nullable: Whale Alert and per-platform baselines have no org row.
-- Btree index on organization_id per FK-index skill rule — supports Phase 12 JOINs.
-- =====================================================================
CREATE TABLE public.collections (
  id              INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  slug            VARCHAR(100)           NOT NULL UNIQUE,
  name            VARCHAR(200)           NOT NULL,
  kind            public.collection_kind,           -- nullable: stubs carry NULL kind (D-09)
  organization_id INTEGER REFERENCES public.organizations(id)  -- nullable: stubs + Whale Alert (D-02)
);

CREATE INDEX ON public.collections (organization_id);

ALTER TABLE public.collections ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Collections are publicly readable"

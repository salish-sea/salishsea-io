-- Phase 9: Reference Table Foundation
-- Creates the provenance graph reference tables (providers, organizations, collections)
-- plus a nullable orcid column on the existing public.contributors.
-- Access control: ENABLE ROW LEVEL SECURITY + permissive SELECT policy per table (D-10).
-- The public schema is already PostgREST-exposed; no config changes needed (Pitfall 5).

-- =====================================================================
-- 1. collection_kind ENUM
-- Exactly five locked values (D-09: the sixth candidate was explicitly rejected).
-- PostgreSQL has no ALTER TYPE ... DROP VALUE; adding an unwanted value now would require a
-- destructive migration to remove. Enum is nullable on collections.kind so stubs
-- (un-decoded acronym channels) carry NULL kind until Phase 11 decodes them.
-- =====================================================================
-- NOTE: 'aggregator_ingest' is intentionally absent from this enum (D-09).
-- PostgreSQL cannot drop enum values; adding it now would require a destructive
-- migration to remove. SC-3 assertion verifies absence via a failed cast.
CREATE TYPE public.collection_kind AS ENUM (
  'facebook_group',
  'research_dataset',
  'acoustic_feed',
  'detector',
  'direct_app'
);

-- =====================================================================
-- 2. public.providers
-- Four fixed ingest providers. No kind column — slug is sufficient for
-- identity; Phase 11 resolver maps slug → behavior in TypeScript (D-discretion).
-- =====================================================================
CREATE TABLE public.providers (
  id   INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  slug VARCHAR(50)  NOT NULL UNIQUE,
  name VARCHAR(100) NOT NULL
);

ALTER TABLE public.providers ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Providers are publicly readable"
  ON public.providers FOR SELECT TO anon, authenticated
  USING (true);


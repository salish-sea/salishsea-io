-- 6. inaturalist.mint_contributor — SECURITY DEFINER helper (D-15, D-16)
--    MANDATORY: SECURITY DEFINER + SET search_path = '' (empty string)
--    Reason: public.contributors has RLS enabled (20260203234153_individuals.sql line 12).
--    No INSERT policy exists for the postgres (pg_cron) role. Without SECURITY DEFINER
--    the INSERT is silently blocked by RLS (RESEARCH Pitfall 2).
--    Precedent: create_contributor_on_sign_in (LANGUAGE plpgsql SECURITY DEFINER SET search_path='')
--    Every object schema-qualified (public.contributors) because search_path = ''.
--    Standalone helper because the iNat ingest function uses BEGIN ATOMIC SQL which
--    cannot use PL/pgSQL control flow (RESEARCH Pitfall 5).
-- =====================================================================
CREATE OR REPLACE FUNCTION inaturalist.mint_contributor(
  inat_login text
) RETURNS integer LANGUAGE SQL VOLATILE SECURITY DEFINER
SET search_path = '' AS $$
  INSERT INTO public.contributors (name, inat_login)
  VALUES (inat_login, inat_login)
  ON CONFLICT (inat_login) DO NOTHING;
  SELECT id FROM public.contributors WHERE contributors.inat_login = $1;
$$;


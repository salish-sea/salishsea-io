-- =====================================================================
-- 1. maplify.collection_rule — the DB-side resolution dictionary (D-03)
--    match_kind IN ('bracket','attribution','source') — locked precedence order
--    match_value TEXT NOT NULL — exact bracket tag text, attribution phrase, or source code
--    collection_id FK → public.collections(id) — enforced at seed time
-- =====================================================================
CREATE TABLE maplify.collection_rule (
  id            INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  match_kind    TEXT    NOT NULL CHECK (match_kind IN ('bracket', 'attribution', 'source')),
  match_value   TEXT    NOT NULL,
  collection_id INTEGER NOT NULL REFERENCES public.collections(id),
  UNIQUE (match_kind, match_value)
);

-- =====================================================================
-- 2. maplify.resolve_collection — thin precedence resolver (D-02, D-03, D-04)
--    Locked precedence: bracket tag → attribution → source code → NULL
--    STABLE: reads collection_rule table; no writes; callable inside INSERT...SELECT
--    Regex ^\[([^\]]+)\] requires non-empty bracket content (D-09 tightening)
-- =====================================================================
CREATE FUNCTION maplify.resolve_collection(
  comments text,
  source   text
) RETURNS integer LANGUAGE SQL STABLE AS $$
  -- Precedence 1: bracket tag (non-empty ^\[([^\]]+)\] per D-09)
  SELECT collection_id FROM maplify.collection_rule
   WHERE match_kind = 'bracket'
     AND comments ~ '^\[([^\]]+)\]'
     AND match_value = (regexp_match(comments, '^\[([^\]]+)\]'))[1]
  UNION ALL
  -- Precedence 2: trailing attribution ("Trusted Observer" phrase)
  SELECT collection_id FROM maplify.collection_rule
   WHERE match_kind = 'attribution'
     AND comments ~ match_value
  UNION ALL
  -- Precedence 3: structured source code
  SELECT collection_id FROM maplify.collection_rule
   WHERE match_kind = 'source'
     AND match_value = source
  LIMIT 1;
$$;

